const { Schema, model } = require('mongoose')

const createUserSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    confirmPassword: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  },
)

const SignUpUser = new model('Signup', createUserSchema)

module.exports = SignUpUser
